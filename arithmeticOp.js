function sum(num1,num2){
    var sum = num1 +num2;
    return sum;
}
function subst(num1,num2){
    var subst = num1 -num2;
    return subst;
}
function multi(num1,num2){
    var multi = num1*num2;
    return multi;
}
function div(num1,num2){
    var div = num1/num2;
    return div;
}
